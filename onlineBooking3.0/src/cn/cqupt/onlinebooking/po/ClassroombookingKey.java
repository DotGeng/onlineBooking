package cn.cqupt.onlinebooking.po;

public class ClassroombookingKey {
    private Integer classroombookingid;

    private Integer roomid;

    private Integer batch;

    private Integer week;

    private Integer periodid;

    public Integer getClassroombookingid() {
        return classroombookingid;
    }

    public void setClassroombookingid(Integer classroombookingid) {
        this.classroombookingid = classroombookingid;
    }

    public Integer getRoomid() {
        return roomid;
    }

    public void setRoomid(Integer roomid) {
        this.roomid = roomid;
    }

    public Integer getBatch() {
        return batch;
    }

    public void setBatch(Integer batch) {
        this.batch = batch;
    }

    public Integer getWeek() {
        return week;
    }

    public void setWeek(Integer week) {
        this.week = week;
    }

    public Integer getPeriodid() {
        return periodid;
    }

    public void setPeriodid(Integer periodid) {
        this.periodid = periodid;
    }
}